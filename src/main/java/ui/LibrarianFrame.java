    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTableRequests.getModel();
        int rowID = jTableRequests.getSelectedRow();
        if (rowID < 0) {
            JOptionPane.showMessageDialog(this, "Select Request");
        } else {
            int rid = Integer.parseInt(dtm.getValueAt(rowID, 0).toString());
            String status = dtm.getValueAt(rowID, 4).toString();
            if (status.equals("REQUESTED")) {
                for (Library lib : Main.branch.getLibraries()) {
                    if (lib.getLibraryID() == Main.LIB_ID) {
                        for (Requests reqs : lib.getRequests()) {
                            if (reqs.getId() == rid) {
                                reqs.setStatus("RENTED");
                                System.out.println(reqs);
                            }
                        }
                    }

                }
            }
        }
        
        populateRequestTable();
    }//GEN-LAST:event_btnAcceptActionPerformed

