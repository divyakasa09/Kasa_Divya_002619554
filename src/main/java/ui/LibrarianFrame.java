    public void populateBooksTable() {
        for (Library lib : Main.branch.getLibraries()) {
            if (lib.getLibraryID() == Main.LIB_ID) {
                DefaultTableModel dtm = (DefaultTableModel) jTableBooks.getModel();
                dtm.setRowCount(0);
                Object[] row = new Object[5];
                for (Material met : lib.getMaterials()) {
                    if (met instanceof Book) {
                        Book b = (Book) met;
                        row[0] = b.getSerialNo();
                        row[1] = b.getName();
                        row[2] = b.getAuthor().getAuthorName();
                        row[3] = b.getGenre().getGenreName();
                        row[4] = b.getRegDate();
                        dtm.addRow(row);
                    }
                }
            }
        }
    }

