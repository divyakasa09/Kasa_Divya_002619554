    public void populateRequestTable() {
        DefaultTableModel dtm = (DefaultTableModel) jTableRequests.getModel();
        dtm.setRowCount(0);
        Object[] row = new Object[5];
        for (Library lib : Main.branch.getLibraries()) {
            if (lib.getLibraryID() == Main.LIB_ID) {
                for (Requests reqs : lib.getRequests()) {                   
                        row[0] = reqs.getId();
                        row[1] = reqs.getPrice();
                        row[2] = reqs.getDuration();
                        row[3] = reqs.getMaterial();
                        row[4] = reqs.getStatus();
                        dtm.addRow(row);                   
                }
            }
        }
    }

    public void populateBooksTable() {
        for (Library lib : Main.branch.getLibraries()) {
            if (lib.getLibraryID() == Main.LIB_ID) {
                DefaultTableModel dtm = (DefaultTableModel) jTableBooks.getModel();
                dtm.setRowCount(0);
                Object[] row = new Object[4];
                for (Material met : lib.getMaterials()) {
                    if (met instanceof Book) {
                        Book b = (Book) met;
                        row[0] = b.getSerialNo();
                        row[1] = b.getName();
                        row[2] = b.getAuthor().getAuthorName();
                        row[3] = b.getGenre().getGenreName();
                        dtm.addRow(row);
                    }
                }
            }
        }
    }

    public void papuateMagazinesTable() {
        for (Library lib : Main.branch.getLibraries()) {
            if (lib.getLibraryID() == Main.LIB_ID) {
                DefaultTableModel dtm = (DefaultTableModel) jTableMagazines.getModel();
                dtm.setRowCount(0);
                Object[] row = new Object[4];
                for (Material met : lib.getMaterials()) {
                    if (met instanceof Magazine) {
                        Magazine m = (Magazine) met;
                        row[0] = m.getSerialNo();
                        row[1] = m.getName();
                        row[2] = m.getCompanyName();
                        row[3] = m.getIssueType();
                        dtm.addRow(row);
                    }
                }
            }
        }
    }

