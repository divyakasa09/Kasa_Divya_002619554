    private void btnTotalRevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalRevActionPerformed
        // TODO add your handling code here:
        float totalRevenue = 0;
        for (Library lib : Main.branch.getLibraries()) {
            if (lib.getLibraryID() == Main.LIB_ID) {
                for (Requests reqs : lib.getRequests()) {
                    if (reqs.getStatus().equals("RENTED") || reqs.getStatus().equals("RETURNED")){
                        totalRevenue+= reqs.getPrice();
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(this, "TOTAL REVENUE : " + totalRevenue);
    }//GEN-LAST:event_btnTotalRevActionPerformed

