    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String applicantionID = fappID.getText();
        String fname=fFirstName.getText();
        String lname=fLastName.getText();
        
        
        String petname=fpetName.getText();
        String petage =fpetAge.getText();
        String gender =fGender.getText();
        String breed =fBreed.getText();
        String petType=fPetType.getText();
        applicant.setApplicationID(Integer.valueOf(applicantionID));
        applicant.setOwnerFirstName(fname);
        applicant.setOwnerLastName(lname);
        
        
        Pet pet = applicant.getPet();
        pet.setPetName(petname);
        pet.setAge(Integer.valueOf(petage));
        pet.setGender(gender);
        pet.setBreed(breed);
        pet.setPetType(petType);
        
         
        
        
    }//GEN-LAST:event_updateBtnActionPerformed


