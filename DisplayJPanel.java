private void populateApplicantDetails()
{
  fappID.setText(String.valueOf(applicant.getApplicationID()));
  fFirstName.setText(applicant.getOwnerFirstName());
  fLastName.setText(applicant.getOwnerLastName());       
//  fdate.setDate(this.applicant.getApplicationDate());
 fpetName.setText(applicant.getPet().getPetName());
  fpetAge.setText(String.valueOf(applicant.getPet().getAge()));
  fGender.setText(applicant.getPet().getGender());
 fPetType.setText(applicant.getPet().getPetType());
 fBreed.setText(applicant.getPet().getBreed());
}
